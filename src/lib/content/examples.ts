/**
 * 콘텐츠 타입별 Few-Shot 예시를 프롬프트용 텍스트로 변환
 */
export function getFewShotExamples(contentType: ContentType): string {
  const examples = CONTENT_EXAMPLES[contentType]
  if (!examples || examples.length === 0) return ''

  let text = `\n\n## 📘 우수 사례 참고 (${examples.length}개)\n`
  text += `아래 예시들을 참고하되, 절대 복사하지 말고 주제에 맞게 변형하세요.\n\n`

  examples.forEach((ex, idx) => {
    text += `### 예시 ${idx + 1}: "${ex.keyword}"\n`
    text += `**제목:** ${ex.title}\n\n`
    text += `**목차 구조:**\n${ex.structure}\n\n`
    text += `**왜 좋은가:** ${ex.why_good}\n\n`
    text += `---\n\n`
  })

  return text
}
